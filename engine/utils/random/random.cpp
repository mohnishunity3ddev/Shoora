u32
shu::rand::GetNext()
{
    this->Index = (this->Index + 1) % TableLength;
    u32 Result = RandomTable[this->Index];
    return Result;
}

u32
shu::rand::NextUInt32()
{
    u32 Result = GetNext();
    return Result;
}

i32
shu::rand::NextInt32()
{
    i32 Result = (i32)GetNext();
    return Result;
}

void
shu::rand::Seed(u32 SeedValue)
{
    this->Index = (SeedValue % TableLength);
}

f32
shu::rand::Range01()
{
    f32 Val = (f32)NextUInt32();

    f32 t = (Val - (f32)MinUnsigned) / (f32)MaxUnsigned;
    ASSERT(t >= 0.0f && t <= 1.0f);

    return t;
}

f32
shu::rand::RangeMinus1To1()
{
    f32 Val = (f32)NextUInt32();

    f32 d = (f32)MaxUnsigned - (f32)MinUnsigned;
    f32 t = ((f32)Val - (f32)MinUnsigned) / d;
    f32 Result = -1.0f + t*2.0f;

    ASSERT(Result >= -1.0f && Result <= 1.0f);

    return Result;
}

f32
shu::rand::RangeBetweenF32(f32 Low, f32 High)
{
    f32 Val = (f32)NextUInt32();

    f32 d = (f32)MaxUnsigned - (f32)MinUnsigned;
    f32 t = (Val - (f32)MinUnsigned) / d;
    f32 Result = Low + t*(High - Low);

    ASSERT(Result >= Low && Result <= High);

    return Result;
}

i32
shu::rand::RangeBetweenInt32(i32 Low, i32 High)
{
    i32 Val = (i32)NextInt32();

    f32 d = (f32)MaxInteger - (f32)MinInteger;
    f32 t = ((f32)Val - (f32)MinInteger) / d;
    i32 Result = Low + t*(High - Low);

    ASSERT(Result >= Low && Result <= High);

    return Result;
}

u32
shu::rand::RangeBetweenUInt32(u32 Low, u32 High)
{
    u32 Val = NextUInt32();

    f32 d = (f32)MaxUnsigned - (f32)MinUnsigned;
    f32 t = ((f32)Val - (f32)MinUnsigned) / d;
    u32 Result = Low + t*(High - Low);

    ASSERT(Result >= Low && Result <= High);

    return Result;
}

